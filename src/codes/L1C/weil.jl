"""
generate_weil(prn::Int64)

Generate the Weil code for a given PRN.

Returns 1D Int64 Array of length 10223.
"""
function generate_weil(prn::Int64)
    W = Array{Int64}(undef, 10223)
    w = weil_parms[prn]['w']
    # XOR L(t) and L(t + w)
    for i in 1:10223
        shift_idx = (i + w) % 10223
        @inbounds W[i] = xor(L_t[i],
                             L_t[if (shift_idx == 0) 10223 else shift_idx end])
    end
    # Insert expansion sequence into W
    p = weil_parms[prn]['p']
    W[p-1:p+5] = expansion_sequence
    return W
end


"""
expansion_sequence

The expansion sequence to be inserted at the p index of
the constructed weil code.

Note that "expasion_sequence" is placed into the following
W indices.

With W_i(t, w):

W_i(p-1, w) = 0
W_i(p,   w) = 1
W_i(p+1, w) = 1
W_i(p+2, w) = 0
W_i(p+3, w) = 1
W_i(p+4, w) = 0
W_i(p+5, w) = 0
"""
